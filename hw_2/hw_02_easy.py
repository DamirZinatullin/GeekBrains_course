# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить
# на два.
list_of_integer = [145, 25, 14, 36, 41]
new_list = [el / 4 if el % 2 == 0 else el * 2 for el in list_of_integer]
print(new_list)
