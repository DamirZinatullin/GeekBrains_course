# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне
fruits = ['яблоко', 'ананас', 'банан', 'апельсин', 'киви',
          'мандаринkjfhtfhgjgjhgchghfghfh', 'помело', 'помидор',
          'огурец', 'перец']
print(fruits)
len_max = len(max(fruits, key=len))
count = 0
for fruit in fruits:
    count += 1
    print('{0}. {1:>{2}}'.format(count, fruit, len_max))

